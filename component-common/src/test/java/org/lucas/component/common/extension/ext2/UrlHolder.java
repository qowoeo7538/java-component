package org.lucas.component.common.extension.ext2;

public class UrlHolder {
    private Double Num;

    private ExtURL url;

    private String name;

    private int age;

    public Double getNum() {
        return Num;
    }

    public void setNum(Double num) {
        Num = num;
    }

    public ExtURL getUrl() {
        return url;
    }

    public void setUrl(ExtURL url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}